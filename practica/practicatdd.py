class Contador():
    def __init__(self, inicial, incremento, limite):
        self.inicio = inicial
        self.incremento = incremento
        self.limite = limite
    def __inicio__(self):
        return self.inicio

    def __incremento__(self):
        return self.incremento

    def __limite__(self):
        return self.limite

    def __incrementar__(self):
        self.inicio = self.inicio + self.incremento
